local tAPIsLoading = {}

CopyTable = function(t)
	local u = { __index = _G }
	for k, v in pairs(t) do
		u[k] = v
	end
	return setmetatable(u, getmetatable(t))
end

function includeFile(_sPath)
	local sName = fs.getName( _sPath )
	if tAPIsLoading[sName] == true then
		printError( "API "..sName.." is already being loaded" )
		return false
	end
	tAPIsLoading[sName] = true

	local tEnv = getfenv()


	setmetatable( tEnv, { __index = _G } )
	tEnv['OSSettings'] = OSSettings
	local fnAPI, err = loadfile( _sPath )
	if fnAPI then
		setfenv( fnAPI, tEnv )
		fnAPI()
	else
		printError( err )
        tAPIsLoading[sName] = nil
		return false
	end
	
	local oldv = nil

	local tAPI = {}
	for k,v in pairs( tEnv ) do
		tAPI[k] =  v
		oldv = k
	end
	_G[sName] = tAPI
	tAPIsLoading[sName] = nil
	if verbose then
		sleep(0)
	end
	return true
end

function include(_sDir)
	if fs.isDir(_sDir) then
		for _, file in pairs(fs.list(_sDir)) do
			if string.sub(file,1,1) ~= "." then
				if fs.isDir(_sDir.."/"..file) then
					include(_sDir.."/"..file)
				else
					includeFile(_sDir.."/"..file)
					if verbose then
						print("Included "..file)
					end
				end
			end
		end
	else
		error("Attempt to dir-include a non-directory")
	end
end

